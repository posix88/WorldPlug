import SwiftUI

/// Consistent spacing values for the app following a design system approach
public extension CGFloat {
    /// 2pt - Extra extra small spacing
    static let xxs: CGFloat = 2
    /// 4pt - Extra small spacing
    static let xs: CGFloat = 4
    /// 6pt - Small spacing
    static let sm: CGFloat = 6
    /// 8pt - Medium spacing
    static let md: CGFloat = 8
    /// 12pt - Large spacing
    static let lg: CGFloat = 12
    /// 16pt - Extra large spacing
    static let xl: CGFloat = 16
    /// 20pt - Extra extra large spacing
    static let xxl: CGFloat = 20
    /// 24pt - Extra extra extra large spacing
    static let xxxl: CGFloat = 24
    /// 32pt - Maximum spacing for major sections
    static let max: CGFloat = 32
    /// 60pt - Special spacing for empty states and major separations
    static let special: CGFloat = 60
}
